    public void Place()
    {
        DestroyPreviousGeneration();

        for(int i = 0; i < tilemap.GetLength(0); i++)
        {
            for(int j = 0; j < tilemap.GetLength(1); j++)
            {
                int index = tilemap[i, j];

                if (index < tiles.Length && tiles[index] != null)
                {
                    GameObject asset = PrefabUtility.InstantiatePrefab(tiles[index]) as GameObject;
                    asset.transform.position = new Vector3(offsetX + i * asset.transform.localScale.x * 10, 0, offsetZ + j * asset.transform.localScale.z * 10);
                    asset.transform.parent = transform;
                }
            }
        }
    }

    private void DestroyPreviousGeneration()
    {
        GameObject[] objectsToDelete = new GameObject[transform.childCount];

        for (int i = 0; i < transform.childCount; i++)
            objectsToDelete[i] = transform.GetChild(i).gameObject;

        foreach (GameObject item in objectsToDelete)
            DestroyImmediate(item);
    }
}
